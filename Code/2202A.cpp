void solve() 
{
  int x, y; cin >> x >> y;
  int p = x;
  if (y > 0) {
    p = x - (2 * y);
  }
  else if (y < 0) {
    p = x - (4 * abs(y));
  }

  if (p % 3 == 0 and p >= 0) yes;
  else no;
}

int32_t main()
{
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);

  int t = 1;
  cin >> t;
  while(t--) solve();

  return 0;
}